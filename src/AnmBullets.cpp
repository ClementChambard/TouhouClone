int getCorrespondingSP1(int sp0)
{
    int col16 = -1;
    int col8 = 0;
    int i = 0;
    if (sp0 < Bspr::s323) col16 = (sp0-Bspr::s0)%16;
    if (sp0 > Bspr::s367 && sp0 < Bspr::s432) col8 = (sp0-Bspr::s368)%8;
    switch(col16){
        case 0: i = 0; break;
        case 1: case 2: i = 1; break;
        case 3: case 4: i = 2; break;
        case 5: case 6: i = 3; break;
        case 7: case 8: i = 4; break;
        case 9: case 10: case 11: i = 5; break;
        case 12: case 13: case 14: i = 6; break;
        case 15: i = 7; break;
    }
    i += col8;
    return Bspr::s323 + i;
}

int getCorrespondingSP2(int sp0)
{
    int col16 = -1;
    int col8 = -1;
    int i = 0;
    sp0--;
    if (sp0 < Bspr::s323) col16 = (sp0-Bspr::s0)%16;
    if (col16 != -1) i = col16;
    return Bspr::s336 + i;
}

